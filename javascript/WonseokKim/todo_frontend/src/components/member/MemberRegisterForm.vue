<template>
    <form @submit.prevent="onSignUp">
        <table>
        <tr>
            <td>아이디</td>
            <td><input type="text" v-model="id"></td>
        </tr>
        <tr>
            <td>비밀번호</td>
            <td><input type="password" v-model="pw"></td>
        </tr>
    </table>

        <div>
            <button type="signup">등록</button>
            <router-link :to="{ name: 'MemberSignupPage' }">
                가입취소
            </router-link>
        </div>
    </form>
</template>

<script>
export default {
    name: 'MemberRegisterForm',
    data () {
        return {
            id: '아이디 입력',
            pw: ''
        }
    },
    methods: {
        onSignUp () {
            const { id, pw } = this
            this.$emit('signup', { id, pw })
        }
    }
}
</script>